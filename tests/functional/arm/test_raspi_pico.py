        vm.set_machine(f'raspi-pico,strict-uart-pins=off,flash-file={flash}')
        vm.set_console()
        vm.add_args(*extra_args)
        if kernel:
            vm.add_args('-kernel', kernel)
        vm.launch()
        wait_for_console_pattern(self, pattern, vm=vm)
        vm.shutdown()

